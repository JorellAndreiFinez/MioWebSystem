    <span class="article-date">Posted October 20, 2018</span>
    <h1 class="article-title">Unionbank Outreach Program</h1>
    <hr>
    <div class="article-content">
        <div class="article-image">
            <img src="https://static.wixstatic.com/media/03665b_0149ef24239e44c3bae09ea83b17717b~mv2.jpg/v1/fill/w_951,h_562,al_c,q_85,enc_avif,quality_auto/IMG_0288_JPG.jpg" alt="Unionbank Outreach Program">
        </div>
        <p>The Unionbank Team, headed by Dwight Barilea and Joanne Yao, conducted an outreach program for our PID Deaf Students. They played a couple of fun games with the kids that they truly enjoyed.</p>
        <p>They even brought Jollibee to entertain the kids. The Unionbank team represented by The Manila Region, BF Homes Branch, Salcedo Branch, Internal Audit Division, Binondo Region, and Performance and Quality Management members...</p>
        <p>They donated 2 brand new 55 inches LCD TV, an LCD projector, and 6 fire extinguishers. They even gave school bags full of school supplies for the kids.</p>
        <p>This was an incredible event that left a lasting impact on the children. The outreach program aimed to provide not only entertainment but also essential resources for their education. The donations were warmly received, and the team hopes to continue such initiatives in the future.</p>
