            <span class="highlight">OUR HISTORY</span>
            <h2>Focus for the Deaf</h2>
        </div>
    </div>
    <div class="history-content">
        <p>
            The primary focus for the deaf children is on learning to listen, talk, read and succeed in mainstream society.
        </p>
        <p>
            Once listening skills are developed, a deaf child can begin to develop speech and language skills that will allow them to communicate in the mainstream.
        </p>
        <p>
            Some people believe that deaf children can do everything but hear, but with proper amplification (hearing aids or cochlear implants) PID and ISP and those committed to the auditory/oral approach believe that deaf children can do everything and hear! This is particularly true today with the advent of powerful hearing aids, cochlear implants and comprehensive <i>One-on-One Speech Therapy</i> in an inclusion setting.
        </p>

        <p>
        PID was founded in 1988 by an outstanding Special Education Teacher, inspired by her mother’s half a century work for the deaf at the Philippine Department of Education, the late Dr. Sergia G. Esguerra, with the help of the 15-Man Board of Directors. It was originally the Julie Esguerra Speech Center [JESC]. Though the dissolution of JESC in 1988 and all its assets donated to the Sergia Esguerra Memorial Foundation, Inc. [SEMFI], with the high cost of One-on-One Speech Therapy and the many indigent qualified deaf children accepted in the programs, it became increasingly difficult to meet the financial needs of the school.
        </p>

